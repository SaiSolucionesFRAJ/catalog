function mostrarDetalles() {
  const productoSeleccionado = document.getElementById("productos-lista").value;
  const detallesDiv = document.getElementById("detalles-producto");

  if (productoSeleccionado && productos[productoSeleccionado]) {
    const producto = productos[productoSeleccionado];
    detallesDiv.innerHTML = `
            <h3>${producto.nombre}</h3>
            <img src="${producto.imagen}" alt="${producto.nombre}" class="producto-imagen">
            <p>${producto.descripcion}</p>
            <p>Precio: ${producto.precio} Dólares</p>
            <button onclick="window.location.href='${producto.enlacePaypal}'">Comprar con PayPal</button>
            <button onclick="window.location.href='${producto.enlaceHotmart}'">Solicitar Compra</button>
            <div class="producto-video">
                <iframe width="560" height="315" src="${producto.video}" frameborder="0" allowfullscreen></iframe>
            </div>
        `;
  } else {
    detallesDiv.innerHTML = "";
  }
}

function E(selector, parent) {
  if (selector instanceof HTMLElement) {
    return selector;
  }

  return (parent || document).querySelectorAll(selector);
}
function hasClass(element, className) {
  return element.classList.contains(className);
}

function radioClass(element, className) {
  E("." + className).forEach((Elem) => Elem.classList.remove(className));
  element.classList.toggle(className);
}

function tabs(nav) {
  let navElem = E(nav)[0];

  navElem.addEventListener("click", (e) => {
    let target = e.target;

    if (hasClass(target, "tab")) radioClass(target, "active");

    let linkedTab = E("." + target.id)[0];

    radioClass(linkedTab, "visible");
  });

  let active = E(".tab.active")[0];
  if (active) {
    radioClass(E("." + active.id)[0], "visible");
  }
}

